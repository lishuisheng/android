package com.lss.example.server;

public class Singer {

	public Singer(){
		
	}
	
	String name = null;
	String song = null;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSong() {
		return song;
	}
	public void setSong(String song) {
		this.song = song;
	}
}
